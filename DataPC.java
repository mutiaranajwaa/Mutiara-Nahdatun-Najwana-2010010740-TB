    public void cetak(){
    Document doc = new Document();
    try {
        PdfWriter.getInstance(doc, new FileOutputStream("laporan-pc.pdf"));
        doc.open();

        Font fontTebal = new Font(Font.FontFamily.HELVETICA, 14, Font.BOLD);
        
        Paragraph jdl = new Paragraph("Laporan data pc", fontTebal);
        jdl.setAlignment(Paragraph.ALIGN_CENTER);
        doc.add(jdl);

        PdfPTable table = new PdfPTable(4);

        doc.add(new Paragraph("\n"));
        
        PdfPCell cellId = new PdfPCell(new Paragraph("Kode"));
        table.addCell(cellId);
        
        PdfPCell cellNama = new PdfPCell(new Paragraph("Nama PC"));
        table.addCell(cellNama);
        
        PdfPCell cellSpek = new PdfPCell(new Paragraph("Spesifikasi"));
        table.addCell(cellSpek);
        
        PdfPCell cellHarga = new PdfPCell(new Paragraph("Harga sewa/hari"));
        table.addCell(cellHarga);
        

        Connection conn = koneksi.getConnection();

        String query = "SELECT * FROM tb_pc";

        Statement statement = conn.createStatement();
        ResultSet hasil = statement.executeQuery(query);

        while (hasil.next()) {
          String id             = hasil.getString("id");
          String nama           = hasil.getString("nama_pc");
          String spesifikasi    = hasil.getString("spesifikasi");
          String harga          = hasil.getString("harga_sewa");

          table.addCell(id);
          table.addCell(nama);
          table.addCell(spesifikasi);
          table.addCell(harga);
        }

        doc.add(table);
        JOptionPane.showMessageDialog(null, "Mencetak data pc Berhasil, file sudah dibuat", "Infromasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            doc.close();
        }
    }
    
