    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nama         = fPC.getText();
        String spek         = fSpek.getText();
        String harga        = fHarga.getText();
        
        if(fPC.getText().equals("") || fSpek.getText().equals("") || fHarga.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Inputan tidak boleh kosong", "Informasi", JOptionPane.YES_OPTION);
        } else {
            try (Connection conn = koneksi.getConnection()) {
                final String tambah_pc = "INSERT INTO tb_pc (nama_pc, spesifikasi, harga_sewa)"
                                          + " VALUES (?, ?, ?)";

                try (PreparedStatement pstmt = conn.prepareStatement(tambah_pc)) {
                  pstmt.setString(1, nama);
                  pstmt.setString(2, spek);
                  pstmt.setString(3, harga);

                  pstmt.executeUpdate();

                  JOptionPane.showMessageDialog(null, "Menyimpan data PC Berhasil", "Infromasi", JOptionPane.INFORMATION_MESSAGE);
                  tampil();
                  bersih();
                } catch (SQLException se) {
                  se.printStackTrace();
                }
              } catch (SQLException se) {
                se.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

