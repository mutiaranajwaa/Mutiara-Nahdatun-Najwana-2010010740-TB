    public void tampil() {

        tabel = new DefaultTableModel();

        tabel.addColumn("Kode");
        tabel.addColumn("Nama PC");
        tabel.addColumn("Spesifikasi");
        tabel.addColumn("Harga sewa/hari");

        final String tampil_pc = "SELECT * FROM tb_pc";

        try (Connection conn = koneksi.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(tampil_pc)) {

          while (rs.next()) {
            int id = rs.getInt("id");
            String nama = rs.getString("nama_pc");
            String spek = rs.getString("spesifikasi");
            String harga = rs.getString("harga_sewa");
            tabel.addRow(new Object[]{id, nama, spek, harga});
          }
        } catch (SQLException se) {
          se.printStackTrace();
        }

        tb_pc.setModel(tabel);
    }
    
