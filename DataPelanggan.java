    public void cetak(){
    Document doc = new Document();
    try {
        PdfWriter.getInstance(doc, new FileOutputStream("laporan-pelanggan.pdf"));
        doc.open();

        Font fontTebal = new Font(Font.FontFamily.HELVETICA, 14, Font.BOLD);
        
        Paragraph jdl = new Paragraph("Laporan data pelanggan", fontTebal);
        jdl.setAlignment(Paragraph.ALIGN_CENTER);
        doc.add(jdl);

        PdfPTable table = new PdfPTable(4);

        doc.add(new Paragraph("\n"));
        
        PdfPCell cellId = new PdfPCell(new Paragraph("Kode"));
        table.addCell(cellId);
        
        PdfPCell cellNama = new PdfPCell(new Paragraph("Nama Pelanggan"));
        table.addCell(cellNama);
        
        PdfPCell cellSpek = new PdfPCell(new Paragraph("Alamat"));
        table.addCell(cellSpek);
        
        PdfPCell cellHarga = new PdfPCell(new Paragraph("No. Telp"));
        table.addCell(cellHarga);
        

        Connection conn = koneksi.getConnection();

        String query = "SELECT * FROM tb_pelanggan";

        Statement statement = conn.createStatement();
        ResultSet hasil = statement.executeQuery(query);

        while (hasil.next()) {
          String id             = hasil.getString("id");
          String nama           = hasil.getString("nama");
          String alamat         = hasil.getString("alamat");
          String telp           = hasil.getString("no_telp");

          table.addCell(id);
          table.addCell(nama);
          table.addCell(alamat);
          table.addCell(telp);
        }

        doc.add(table);
        JOptionPane.showMessageDialog(null, "Mencetak data pelanggan Berhasil, file sudah dibuat", "Infromasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            doc.close();
        }
    }
    
