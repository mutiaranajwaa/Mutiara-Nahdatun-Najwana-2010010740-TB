    public void tampil() {

        tabel = new DefaultTableModel();

        tabel.addColumn("Kode");
        tabel.addColumn("Nama Pelanggan");
        tabel.addColumn("Alamat");
        tabel.addColumn("No. Telp");

        final String tampil_pelanggan = "SELECT * FROM tb_pelanggan";

        try (Connection conn = koneksi.getConnection();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(tampil_pelanggan)) {

          while (rs.next()) {
            int id = rs.getInt("id");
            String nama = rs.getString("nama");
            String alamat = rs.getString("alamat");
            String telp = rs.getString("no_telp");
            tabel.addRow(new Object[]{id, nama, alamat, telp});
          }
        } catch (SQLException se) {
          se.printStackTrace();
        }

        tb_pelanggan.setModel(tabel);
    }
    
